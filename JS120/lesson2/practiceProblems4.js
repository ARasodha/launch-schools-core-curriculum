// Practice Problems: Dealing with Context Loss
// Exercise 1:
let turk = {
  firstName: 'Christopher',
  lastName: 'Turk',
  occupation: 'Surgeon',
  getDescription() {
      return this.firstName + ' ' + this.lastName + ' is a '
                                  + this.occupation + '.';
  }
};

function logReturnVal(func) {
  let returnVal = func();
  console.log(returnVal);
}

logReturnVal(turk.getDescription); // logs undefined  undefined is a undefined.
// Explanation: The way the returnVal in the logReturnVal function is invoked sets the context to 
// the global context. Therefore, the values that are trying to be returned are undefined

// Exercise 2:
let turk = {
  firstName: 'Christopher',
  lastName: 'Turk',
  occupation: 'Surgeon',
  context: this,
  getDescription() {
      return this.firstName + ' ' + this.lastName + ' is a '
                                  + this.occupation + '.';
  }
};

function logReturnVal(func, context) { // <--
  let returnVal = func.call(context); // <--
  console.log(returnVal);
}

logReturnVal(turk.getDescription, turk); // Christopher Turk is a Surgeon.

